
# Operators are used to perform operations on variables and values.
# + operator to add two value

a = 7
b = 14

print(a+b)

# Arithmetic operation are
# + (Addition), - (Substraction )
# * (Multiplication), / (Division)	, % (Modulus)
# ** (Exponentiation), // (Floor division)

print(b-a)
print(b*a)
print(b/a)
print(b % a)

# Python Comparison Operators
# Comparison operators are used to compare two values

print(b == a)
print(b != a)

# Python Logical Operators are "and", "or" , "not"
# Logical operators are used to combine conditional statements

print(a < 5 and b < 10)
print(a > 5 or b < 10)

# membership operator
x = ["Apple", "banana"]
print("banana" in x)

# opeator Precedence- order in which operator are performed
print((6 + 3) - (6 + 3))
# *  /  //  %	Multiplication, division, floor division, and modulus in this order
