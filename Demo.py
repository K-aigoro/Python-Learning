
# Variable
a = 5
b = 6
print(a+b)

# Casting - Specify the data type of a variable
x = str(3)    # x will be '3'
y = int(3)    # y will be 3
z = float(3)  # z will be 3.0

print(x)
print(y)
print(z)

# Camel Case - Each word, except the first, starts with a capital letter:
SureName = "Joe"
firstName = "Sam"

# Many Values to Multiple Variables
c, d, e = "Orange", "Banana", "Cherry"
print(c)
print(d)
print(e)

# Unpack a Collection
fruits = ["apple", "orange", "cherry"]
g, h, i = fruits
print(g)
print(h)
print(i)
print(type(fruits))

# Python Number
x = 1    # int
y = 2.8  # float
z = 1j   # complex

# Type coversion

w = 20
j = 21.93
print(float(w))
print(int(j))
